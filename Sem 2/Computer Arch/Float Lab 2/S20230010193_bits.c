/*
 * floatPower2 - Return bit-level equivalent of the expression 2.0^x
 *   (2.0 raised to the power x) for any 32-bit integer x.
 *
 *   The unsigned value that is returned should have the identical bit
 *   representation as the single-precision floating-point number 2.0^x.
 *   If the answer is too small to be represented as a denorm, return
 *   0. If too large, return +INF.
 *
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. Also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned floatPower2(int x)
{
   unsigned bias = 127;
   unsigned maximum = 254;
   int original = x + bias;
   unsigned answer = original << 23;

   if (original < 0)
   {
      return 0;
   }

   if (original > maximum)
   {
      return 0x7F800000;
   }
   return answer;
}

/*
 * floatScale1d2 - Return bit-level equivalent of expression 0.5*f for
 *   floating point argument f.
 *   Both the argument and answer are passed as unsigned int's, but
 *   they are to be interpreted as the bit-level representation of
 *   single-precision floating point values.
 *   When argument is NaN, return argument
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned floatScale1d2(unsigned uf)
{
   unsigned a;
   unsigned b;
   unsigned c;
   unsigned d;
   a = uf & 0x7F800000;
   b = uf & 0x7F000000;
   c = uf & 3;
   if (a == 0x7F800000)
   {
      return uf;
   }
   else if (b)
   {
      d = uf + 0xFF800000;
   }
   else
   {
      d = uf >> 1;
      if (c == 3)
      {
         d += 1;
      }
      if (uf & 0x80000000)
      {
         d += (0x40000000);
      }
   }
   return d;
}

